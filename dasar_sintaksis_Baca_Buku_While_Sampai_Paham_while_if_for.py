"""
Program Perulangan membaaca buku dengan while sampai paham
"""
jumlah_buku = 20
print(f'jumlah Buku {jumlah_buku}')
print('Ibu berkata, "baca semua buku')


jumlah_buku_yang_sudah_dibaca_dan_dipahami = 0
print(f'jumlah buku yang sudah dibaca {jumlah_buku_yang_sudah_dibaca_dan_dipahami}')

while jumlah_buku_yang_sudah_dibaca_dan_dipahami < jumlah_buku :
    jumlah_buku_yang_sudah_dibaca_dan_dipahami = jumlah_buku_yang_sudah_dibaca_dan_dipahami + 1
    print(f"Buku Ke {jumlah_buku_yang_sudah_dibaca_dan_dipahami} sudah di Baca dan dipahami")

## while : selama kondisi belum terpenuhi maka program berjalan sampai kondisi terpenuhi #

print ("SINTAKSIS MENGGUNAKAN WHILE, adalah : "
       "Selama kondisi belum terpenuhi maka program akan berjalan sampai kondisi terpenuhi.")


